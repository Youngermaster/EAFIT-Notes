# Valor exacto de x:
x = 3/22
print("Valor exacto de x:", x)

# Aproximación con 8 decimales correctos:
x_aprox = 0.13636369
print("Aproximación con 8 decimales correctos:", x_aprox)

# Error absoluto:
error_absoluto = abs(x - x_aprox)
print("Error absoluto:", error_absoluto)

# Error relativo:
error_relativo = error_absoluto / abs(x)
print("Error relativo:", error_relativo)
