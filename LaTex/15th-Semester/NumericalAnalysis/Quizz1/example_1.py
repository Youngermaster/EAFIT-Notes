# Valor exacto de x:
x = 7/13
print("Valor exacto de x:", x)

# Aproximación con 13 cifras significativas:
x_aprox = 0.5384615384615
print("Aproximación con 13 cifras significativas:", x_aprox)

# Error absoluto:
error_absoluto = abs(x - x_aprox)
print("Error absoluto:", error_absoluto)

# Error relativo:
error_relativo = error_absoluto / abs(x)
print("Error relativo:", error_relativo)
